-- View for Coeus compatibility 
CREATE OR REPLACE VIEW OSP$QUESTION AS SELECT 
    QUESTION_ID, 
    QUESTION, 
    MAX_ANSWERS, 
    VALID_ANSWER, 
    LOOKUP_NAME, 
    QUESTION_TYPE_NAME AS ANSWER_DATA_TYPE, 
    ANSWER_MAX_LENGTH, 
    LOOKUP_GUI, 
    UPDATE_TIMESTAMP, 
    UPDATE_USER, 
    GROUP_TYPE_CODE
FROM QUESTION A,
     QUESTION_TYPES B
WHERE A.QUESTION_TYPE_ID = B.QUESTION_TYPE_ID;
