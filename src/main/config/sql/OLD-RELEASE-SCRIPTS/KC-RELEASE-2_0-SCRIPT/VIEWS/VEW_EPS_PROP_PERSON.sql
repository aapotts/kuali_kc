CREATE OR REPLACE VIEW OSP$EPS_PROP_KEY_PERSONS AS 
SELECT PROPOSAL_NUMBER,
       CASE WHEN PERSON_ID IS NULL THEN CAST (ROLODEX_ID AS char) ELSE PERSON_ID END PERSON_ID,
       FULL_NAME,
       PRIMARY_TITLE,
       IS_FACULTY,
       CASE WHEN PERSON_ID IS NULL THEN 'N' ELSE 'Y' END NON_MIT_PERSON_FLAG,
       CASE WHEN IS_OSC = 'Y' THEN 999.00 ELSE PERCENTAGE_EFFORT END PERCENTAGE_EFFORT,
       UPDATE_TIMESTAMP,
       UPDATE_USER
FROM   EPS_PROP_PERSON
WHERE  PROP_PERSON_ROLE_ID = 'KP';