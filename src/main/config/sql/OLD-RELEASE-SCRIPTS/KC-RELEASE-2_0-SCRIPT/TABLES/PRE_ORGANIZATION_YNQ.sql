CREATE TABLE TMP_TBL_ORGANIZATION_YNQ AS
SELECT ORGANIZATION_ID, QUESTION_ID, ANSWER, to_lob(EXPLANATION) EXPLANATION, REVIEW_DATE,
UPDATE_TIMESTAMP, UPDATE_USER, VER_NBR, OBJ_ID from ORGANIZATION_YNQ;
ALTER TABLE ORGANIZATION_YNQ DROP (EXPLANATION);
ALTER TABLE ORGANIZATION_YNQ ADD (EXPLANATION CLOB);
UPDATE ORGANIZATION_YNQ TN 
SET (TN.EXPLANATION) = (SELECT EXPLANATION 
FROM TMP_TBL_ORGANIZATION_YNQ OT 
WHERE TN.ORGANIZATION_ID = OT.ORGANIZATION_ID and TN.QUESTION_ID = OT.QUESTION_ID);
DROP TABLE TMP_TBL_ORGANIZATION_YNQ;

