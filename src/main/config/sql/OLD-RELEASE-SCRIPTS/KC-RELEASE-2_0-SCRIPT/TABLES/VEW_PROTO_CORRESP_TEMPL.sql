-- View for Coeus compatibility 
CREATE OR REPLACE VIEW OSP$PROTO_CORRESP_TEMPL AS 
SELECT PROTO_CORRESP_TEMPL.PROTO_CORRESP_TYPE_CODE,
       COMMITTEE.COMMITTEE_ID,
       PROTO_CORRESP_TEMPL.CORRESPONDENCE_TEMPLATE AS CORESPONDENCE_TEMPLATE,
       PROTO_CORRESP_TEMPL.UPDATE_TIMESTAMP,
       PROTO_CORRESP_TEMPL.UPDATE_USER
FROM   PROTO_CORRESP_TEMPL,
       COMMITTEE
WHERE  PROTO_CORRESP_TEMPL.COMMITTEE_ID_FK = COMMITTEE.ID;