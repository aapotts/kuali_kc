CREATE OR REPLACE FORCE VIEW "OSP$PROTOCOL_DOCUMENTS"("PROTOCOL_NUMBER","SEQUENCE_NUMBER","TYPE_CD","DESCRIPTION","FILE_NAME","FILE_DATA","UPDATE_TIMESTAMP","UPDATE_USER","VERSION_NUMBER","STATUS_CD","DOCUMENT_ID") AS 
SELECT PAP.PROTOCOL_NUMBER,
       PAP.SEQUENCE_NUMBER,
       PAP.TYPE_CD,
       PAP.DESCRIPTION,
       PAF.FILE_NAME,
       PAF.FILE_DATA,
       PAP.UPDATE_TIMESTAMP,
       PAP.UPDATE_USER,
       PAF.SEQUENCE_NUMBER VERSION_NUMBER,
       PAP.STATUS_CD,
       PAP.DOCUMENT_ID
FROM   PROTOCOL_ATTACHMENT_PROTOCOL PAP,
       ATTACHMENT_FILE PAF
WHERE  PAP.FILE_ID = PAF.FILE_ID 
       AND PAP.TYPE_CD = '9';
       