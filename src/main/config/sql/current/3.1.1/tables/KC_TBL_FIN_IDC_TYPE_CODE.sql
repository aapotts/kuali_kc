DECLARE temp NUMBER;
BEGIN
        SELECT COUNT(*) INTO temp FROM user_tables WHERE table_name = 'FIN_IDC_TYPE_CODE';
        IF temp > 0 THEN EXECUTE IMMEDIATE 'DROP TABLE FIN_IDC_TYPE_CODE CASCADE CONSTRAINTS PURGE'; END IF;
END;
/

CREATE TABLE FIN_IDC_TYPE_CODE
  ( 
    FIN_IDC_CODE varchar2(5) not null,
    RATE_TYPE_CODE varchar2(3) not null,
    OBJ_ID VARCHAR2(36),
    VER_NBR NUMBER(8,0) NOT NULL,
    RATE_CLASS_CODE VARCHAR2(3) not null
  )
/