INSERT INTO UNIT (UNIT_NUMBER, UNIT_NAME, UPDATE_TIMESTAMP, UPDATE_USER, VER_NBR, OBJ_ID)
SELECT '000001','University',SYSDATE,USER,1,SYS_GUID() FROM DUAL
WHERE NOT EXISTS(SELECT 's' FROM UNIT WHERE UNIT_NUMBER = '000001')
/

