CREATE OR REPLACE VIEW OSP$PROTOCOL_DOCUMENTS AS 
SELECT PRO.PROTOCOL_NUMBER,
       PRO.SEQUENCE_NUMBER,
       PAP.TYPE_CD,
       PAP.DESCRIPTION,
       PAF.FILE_NAME,
       PAF.FILE_DATA,
       PAP.UPDATE_TIMESTAMP,
       PAP.UPDATE_USER,
       PAP.SEQUENCE_NUMBER AS VERSION_NUMBER,
       PAP.STATUS_CD,
       PAP.DOCUMENT_ID
FROM   PROTOCOL_ATTACHMENT_PROTOCOL PAP,
       PROTOCOL_ATTACHMENT_FILE PAF,
       PROTOCOL_ATTACHMENT_PTA_TO_PRO PTA,
       PROTOCOL PRO
WHERE  PAP.FILE_ID = PAF.PA_FILE_ID 
       AND PAP.TYPE_CD <> '9' 
       AND PAP.PA_PROTOCOL_ID = PTA.PA_PROTOCOL_FK 
           AND PRO.PROTOCOL_ID = PTA.PROTOCOL_ID_FK; 
