CREATE OR REPLACE VIEW OSP$comm_schedule_minutes AS 
SELECT SCHEDULE_ID,
       ENTRY_NUMBER,
       MINUTE_ENTRY_TYPE_CODE,
       PROTOCOL_NUMBER,
       SEQUENCE_NUMBER,
       SUBMISSION_NUMBER,
       PRIVATE_COMMENT_FLAG,
       PROTOCOL_CONTINGENCY_CODE,
       MINUTE_ENTRY,
       UPDATE_TIMESTAMP,
       UPDATE_USER
FROM   COMM_SCHEDULE_MINUTES; 

