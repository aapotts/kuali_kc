CREATE OR REPLACE VIEW OSP$TEMPLATE_DOCUMENT_TERMS(TEMPLATE_CODE,REFERENCED_DOCUMENT_CODE,UPDATE_TIMESTAMP,UPDATE_USER) AS 
SELECT A.AWARD_TEMPLATE_CODE,
       B.SPONSOR_TERM_CODE REFERENCED_DOCUMENT_CODE,
       A.UPDATE_TIMESTAMP,
       A.UPDATE_USER
FROM   AWARD_TEMPLATE_TERMS A,
       SPONSOR_TERM B,
       SPONSOR_TERM_TYPE C
WHERE  A.SPONSOR_TERM_ID = B.SPONSOR_TERM_ID 
       AND B.SPONSOR_TERM_TYPE_CODE = C.SPONSOR_TERM_TYPE_CODE 
           AND C.SPONSOR_TERM_TYPE_CODE = '6';

