 CREATE TABLE EPS_PROP_PER_CREDIT_SPLIT (
    PROPOSAL_NUMBER              NUMBER(12)   CONSTRAINT EPS_PROP_PER_CREDIT_SPLIT_N1 not null,
    INV_CREDIT_TYPE_CODE         VARCHAR2(3)    constraint EPS_PROP_PER_CREDIT_SPLIT_N3 not null,
    PROP_PERSON_NUMBER           NUMBER(12)   constraint EPS_PROP_PER_CREDIT_SPLIT_N4 NOT NULL,
    CREDIT                       NUMBER(5,2),
	UPDATE_TIMESTAMP DATE constraint EPS_PROP_PER_CREDIT_SPLIT_N6 NOT NULL ENABLE,
	UPDATE_USER VARCHAR2(8) constraint EPS_PROP_PER_CREDIT_SPLIT_N7 NOT NULL ENABLE,
	VER_NBR NUMBER(8,0) DEFAULT 1  constraint EPS_PER_UNIT_CREDIT_SPLIT_N8  NOT NULL ENABLE,
	OBJ_ID VARCHAR2(36) DEFAULT SYS_GUID() NOT NULL,
    CONSTRAINT EPS_PROP_PER_CREDIT_SPLIT_C0 UNIQUE (OBJ_ID) NOT DEFERRABLE INITIALLY IMMEDIATE,
	CONSTRAINT EPS_PROP_PER_CREDIT_SPLIT_P1 PRIMARY KEY (PROPOSAL_NUMBER, PROP_PERSON_NUMBER, INV_CREDIT_TYPE_CODE) ENABLE
);
