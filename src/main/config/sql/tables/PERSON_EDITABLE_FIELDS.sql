CREATE TABLE PERSON_EDITABLE_FIELDS (
    FIELD_NAME         VARCHAR2(30)  constraint PERSON_EDITABLE_FIELDS_N1 not null,
    ACTIVE_FLAG        CHAR(1),
	UPDATE_TIMESTAMP DATE constraint PERSON_EDITABLE_FIELDS_N2 NOT NULL ENABLE,
	UPDATE_USER VARCHAR2(60) constraint PERSON_EDITABLE_FIELDS_N3 NOT NULL ENABLE,
	VER_NBR NUMBER(8,0) DEFAULT 1  constraint  PERSON_EDITABLE_FIELDS_N4  NOT NULL ENABLE,
	OBJ_ID VARCHAR2(36) DEFAULT SYS_GUID() NOT NULL,
    CONSTRAINT PERSON_EDITABLE_FIELDS_C0 UNIQUE (OBJ_ID) NOT DEFERRABLE INITIALLY IMMEDIATE,
	CONSTRAINT PERSON_EDITABLE_FIELDS_P1 PRIMARY KEY (FIELD_NAME) ENABLE
);
